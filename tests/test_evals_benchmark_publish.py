from __future__ import annotations


def test_top_level_benchmark_alias_help() -> None:
    """Top-level ``cutctx benchmark`` alias must be wired."""
    from click.testing import CliRunner

    from cutctx.cli.main import main

    runner = CliRunner()
    result = runner.invoke(main, ["benchmark", "--help"])
    assert result.exit_code == 0, f"Exit {result.exit_code}: {result.output}"
    assert "benchmark" in result.output.lower()


def test_publish_replaces_only_current_day_section(tmp_path, monkeypatch) -> None:
    """Re-publishing same day should replace one block without eating nested H2s."""
    from datetime import date, timedelta

    import cutctx.cli.evals as cli_evals

    fake_root = tmp_path / "repo"
    docs_dir = fake_root / "docs"
    docs_dir.mkdir(parents=True)
    fake_cli_dir = fake_root / "cutctx" / "cli"
    fake_cli_dir.mkdir(parents=True)
    fake_evals_file = fake_cli_dir / "evals.py"
    fake_evals_file.write_text("# test\n", encoding="utf-8")

    today = date.today()
    next_day = today + timedelta(days=1)
    today_heading = f"## `cutctx evals benchmark` — {today.isoformat()}"
    next_heading = f"## `cutctx evals benchmark` — {next_day.isoformat()}"
    docs_path = docs_dir / "benchmarks.md"
    docs_path.write_text(
        "# Cutctx Benchmarks\n\n"
        "Intro copy.\n\n"
        f"{today_heading}\n\n"
        "Datasets: stale · Compressors: stale · Seed: 0\n\n"
        "## Compression Ratio by Dataset × Compressor\n\n"
        "| stale |\n\n"
        "## F1 Score by Dataset × Compressor\n\n"
        "| stale-f1 |\n\n"
        "_Generated by `cutctx evals benchmark`_\n\n"
        f"{next_heading}\n\n"
        "Future section body.\n",
        encoding="utf-8",
    )

    monkeypatch.setattr(cli_evals, "__file__", str(fake_evals_file))

    cli_evals._publish_benchmark_results(
        "# Cutctx Compressor Benchmark Report\n\n"
        "## Compression Ratio by Dataset × Compressor\n\n"
        "| fresh |\n\n"
        "## F1 Score by Dataset × Compressor\n\n"
        "| fresh-f1 |\n\n"
        "_Generated by `cutctx evals benchmark`_",
        seed=42,
        datasets=["ToolOutputSamples"],
        compressors=["content_router"],
    )

    content = docs_path.read_text(encoding="utf-8")
    assert content.count(today_heading) == 1
    assert content.count(next_heading) == 1
    assert "Datasets: ToolOutputSamples · Compressors: content_router · Seed: 42" in content
    assert "| fresh |" in content
    assert "| fresh-f1 |" in content
    assert "| stale |" not in content
    assert "| stale-f1 |" not in content
    assert "Future section body." in content


def test_publish_bootstraps_benchmarks_doc_when_missing(tmp_path, monkeypatch) -> None:
    """Publish should create docs/benchmarks.md when it does not exist yet."""
    import cutctx.cli.evals as cli_evals

    fake_root = tmp_path / "repo"
    fake_cli_dir = fake_root / "cutctx" / "cli"
    fake_cli_dir.mkdir(parents=True)
    fake_evals_file = fake_cli_dir / "evals.py"
    fake_evals_file.write_text("# test\n", encoding="utf-8")

    monkeypatch.setattr(cli_evals, "__file__", str(fake_evals_file))

    cli_evals._publish_benchmark_results(
        "# Cutctx Compressor Benchmark Report\n\n_Generated by `cutctx evals benchmark`_",
        seed=7,
        datasets=["ToolOutputSamples"],
        compressors=["smart_crusher"],
    )

    docs_path = fake_root / "docs" / "benchmarks.md"
    content = docs_path.read_text(encoding="utf-8")
    assert content.startswith("# Cutctx Benchmarks")
    assert "ToolOutputSamples" in content
    assert "smart_crusher" in content
